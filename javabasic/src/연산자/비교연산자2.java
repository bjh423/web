public class 비교연산자2 {

	public static void main(String[] args) {
		int x = 1;
		int y = 2;
		
		System.out.println("같은가? " + (x == y));
		System.out.println("같지않은가? " + (x != y));
		System.out.println("큰가? " + (x > y));
		System.out.println("크거나같은가? " + (x >= y));
		
	}

}
