package 변수;

public class 변수선언 {

	public static void main(String[] args) {
		//변수를 만들 때, 데이터 타입을 설정
		int num = 100;
		double num2 = 22.2;
		char c = 'a';
		boolean b = true;
		num = 200;
		System.out.println("정수값은 " + num);
		System.out.println("실수값은 " + num2);
		System.out.println("문자 1개값은 " + c);
		System.out.println("불린값은 " + b);
	}

}
